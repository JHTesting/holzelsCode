// This program will print stars according to a variable number

public class starMethods {
	public static void printStars(int amount) {

	
		int counter = 0; //Keeps track of how many times code is activated

		while (counter < amount) {
			System.out.print("*");
		

			counter++; //Keep track of how many times the code has been activated
		
		}

	
	
	
	
	}
	public static void main(String[] args) {
		printStars(5);
		System.out.println(" ");
		printStars(3);
		System.out.println(" ");
		printStars(9);
		System.out.println(" ");
	
	
	}

















}
